    private void btnAddOrUpdateMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddOrUpdateMouseReleased
        JButton target = (JButton) evt.getSource();
        if (target.getText().equalsIgnoreCase("Add")) {
            Boolean result = _ProductService.Create(GetProduct());
            if (result) {
                GetProductTable(null, null);
            }
        } else if (target.getText().equalsIgnoreCase("update")) {
            Boolean result = _ProductService.Update(GetProduct());
            if (result) {
                GetProductTable(null,null);
                BindProduct(new ProductDto("", 0.0, Status.Deactive.name()));
                btnAddOrUpdate.setText("Add");
                btnDelete.setEnabled(false);
            }
        }

    }//GEN-LAST:event_btnAddOrUpdateMouseReleased

    private void btnDeleteMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseReleased
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure!!", "Warning", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            int id = Integer.parseInt(lbId.getText());
            if (_ProductService.Delete(id)) {
                GetProductTable(null,null);
                BindProduct(new ProductDto("", 0.0, Status.Deactive.name()));
                btnDelete.setEnabled(false);
                btnAddOrUpdate.setText("Add");
            }

        }
    }//GEN-LAST:event_btnDeleteMouseReleased

