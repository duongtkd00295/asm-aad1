    private ProductDto GetProduct() {
        String name = txtName.getText();
        Double price;
        try {
            price = Double.parseDouble(txtPrice.getText());
        } catch (Exception e) {
            return null;
        }
        String status = cbStatus.isSelected() ? Status.Active.name() : Status.Deactive.name();
        int id = 0;
        try {
            id = Integer.parseInt(lbId.getText());
        } catch (Exception e) {
        }
        ProductDto dto = new ProductDto(id, name, price, status);
        return dto;
    }

    private void BindProduct(ProductDto dto) {
        txtName.setText(dto.getName());
        txtPrice.setText(dto.getPrice().toString());
        cbStatus.setSelected(dto.getStatus().equals(Status.Active.name()) ? true : false);
        lbId.setText(String.valueOf(dto.getId()));
    }

